/**
 * friend function
 * 1. A friend function of a class is defined outside that class' scope but it has the right to access all private and protected members of the class.
 * 2. Even though the prototypes for friend functions appear in the class definition, friends are not member functions.
 * 3. A friend can be a function, function template, or member function, or a class or class template, in which case the entire class and all of its members are friends.
 * 4. If you want to allow a function to access your class's private data, you can declare the function as a friend.
 * 5. A friend function is declared by the class that is granting access. It is defined like a normal function outside the class.
 */
